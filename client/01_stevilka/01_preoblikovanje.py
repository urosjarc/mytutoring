def str_int(a: str) -> float:
	"""
	Pretvori besedo v stevilko.
	:param a: Beseda
	:return: Decimalka
	>>> str_int('123.45')
	123.45
	>>> str_int('54.321')
	54.321
	"""
	return float(a)


